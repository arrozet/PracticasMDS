/**
 * Enumeración que representa los posibles estados de un animal en el refugio.
 */
public enum EstadoAnimal {
	/**
	 * Estado que indica que el animal está disponible para ser adoptado.
	 */
	disponible,

	/**
	 * Estado que indica que el animal ha sido adoptado.
	 */
	adoptado,

	/**
	 * Estado que indica que el animal está recibiendo tratamiento médico y no está disponible para adopción.
	 */
	enTratamiento
}
